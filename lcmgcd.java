public class eighteen {
    //gcd//
    public static void main(String[]args){
    int n1=60;
    int n2=36;
    int num1=n1;
    int num2=n2;
    int lcm;
    
    while(n1!=n2){
        if (n1>n2)
        {
            n1=n1-n2;
        }
        else{
            n2=n2-n1;
        }}
System.out.println("gcd :"+n1);


lcm=(num1*num2)/n1;
   System.out.println("lcm :"+lcm);
 }
}




